<h3 class="h3TabContent">M Series Range Hoods</h3>
<h4>These wall-mount styles are ideal over standard and professional-style cooking equipment.</h4>
<img src="images/vah/prh18-300x300.jpg" alt="Izth by Vent-A-Hood" class="imageFloat2" />
<p>The M Series Hood features a stainless steel canopy with a baffle filter/blower system that offers high air flow volume in 600 - 1000 CFM options. The canopy is 18-inches high, 24 inches deep and comes in standard widths of 30, 36, 42, 48, 54, 60 and 66 inches. Standard duct covers are also available for 8, 9 or 10-foot ceilings and are priced separately.</p>
<p>Also available is a wall mount liner insert. It has the ability to be covered in a variety of materials and finishes [brick, stone, stucco, cabinetry and more] which stretch the boundaries of kitchen design.</p>
