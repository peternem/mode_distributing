<h3 class="h3TabContent">AGA Total Control TC5 Cooker</h3>
<h4>Introducing the New Five Oven Total Control Range Cooker</h4>
<img src="images/aga/tc5-series-blk-cooker-450x450.jpg" alt="AGA Total Control Cookers" class="imageFloat2 amerImgSm" />
<p>Good things come in big packages, and that’s especially true when it comes 
to cooking. Now the TC5, the latest model in the Total Control series, 
brings you ﬁ ve large ovens, two hotplates and a warming plate to deliver 
incredible capacity and ﬂ exibility.</p>
<p>To create the TC5, we’ve added two ovens and a warming plate to the 
popular TC3. Imagine being able to roast, bake, simmer, slow cook, boil, 
and warm all at the same time!  The TC5’s roasting, baking, and simmering 
ovens are operated and programmed with the touch-screen panel and 
remote control. The additional slow cook oven, warming oven, and 
warming plate are operated as an independent unit, providing the perfect 
environment for casseroles, roasts, soups or stews, and for keeping food 
and dishes warm and ready to serve.</p>
<p>And as with all AGA Range Cookers, the TC5 is a design icon recognized 
throughout the world. Available in 12 amazing colors, there’s sure to be an 
AGA Total Control perfect for your kitchen.</p>

