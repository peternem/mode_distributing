<h3 class="h3TabContent">Cookers</h3>
<h4>The classic cast iron cooker</h4>
<img src="images/aga-cooker-296x300.jpg" alt="AGA Cookers" class="imageFloat2" />
<p>AGA Cookers have been considered a member of peoples family since it was first designed. It has heated kettles and warmed hearts. It has gathered family and friends. It has become the heart of the home. This is why AGA owners all over the world say they couldn't imagine life without one.</p>
<p>Every AGA Cooker is made in exactly the same way it has always been: molten iron is poured into molds to create cast-iron. It's this iron that brings the AGA cooker's distinctive cooking performance and while most other cookers are spray painted in minutes, skilled AGA craftsman add multiple coats of vitreous enamel - a process that takes three days to complete.</p>

